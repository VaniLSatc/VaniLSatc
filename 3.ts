// Функция для проверки цифр числа на делимость на 3 и суммирования подходящих цифр
function check3(A: number, sum: { value: number }): void {
    if (A < 0) A = A * (-1); // Для работы с суммой требуется проверить - положительное число или нет
    // Основной цикл. В нём проверяется, делится ли последняя цифра числа на 3 или нет,
    // и в случае успеха число прибавляется к сумме
    for (let i = 0; i <= 10; i++) {
        if ((A % 10) % 3 === 0) {
            sum.value += (A % 10);
            A = Math.floor(A / 10);
        } else {
            A = Math.floor(A / 10);
        }
    }
}

console.log("Введите количество чисел, а затем сами числа");
const sum = { value: 0 }; // Используем объект для передачи по ссылке (аналог указателя в C++)

// Ввод количества чисел
const countInput = prompt("Введите количество чисел:") || "";
const count = parseInt(countInput);

if (count >= 0) { // Проверка на корректность введённых данных
    // Цикл для ввода чисел
    for (let i = 1; i <= count; i++) {
        const numInput = prompt(`Введите число ${i}:`) || "";
        const num = parseInt(numInput);
        check3(num, sum);
    }
    console.log(`\nРезультат: ${sum.value}`); // Вывод результата
} else {
    console.log("Количество чисел должно быть положительным числом!");
}
